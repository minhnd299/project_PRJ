package Model;

/**
 *
 * @author DPV
 */
public class Cart {

    private Book book;

    public Cart() {
    }

    public Cart(Book book) {
        this.book = book;
    }

    public Book getBook() {
        return book;
    }

    public void setBook(Book book) {
        this.book = book;
    }

    @Override
    public String toString() {
        return "Cart{" + "book=" + book + '}';
    }

}
